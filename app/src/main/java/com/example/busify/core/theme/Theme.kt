private val BusifyShapes = Shapes(
    extraSmall = RoundedCornerShape(4.dp),    // sm: badges, indicators
    small = RoundedCornerShape(8.dp),         // DEFAULT/md: buttons, chips, checkboxes
    medium = RoundedCornerShape(12.dp),       // md
    large = RoundedCornerShape(16.dp),        // lg: cards, input fields, large containers
    extraLarge = RoundedCornerShape(24.dp),   // xl
)

@Composable
fun BusifyTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val colorScheme = if (darkTheme) DarkColorScheme else LightColorScheme
    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.surfaceDim.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        shapes = BusifyShapes,
        content = content
    )
}
